int	ft_sqrt(int nb)
{
	long int	i;
	long int	t;

	t = nb;
	i = 2;
	if (t == 1 || i == 0)
		return (t);
	while (i * i <= t)
	{
		if (t == i * i)
			return (i);
		i++;
	}
	return (0);
}
